package com.matrubhoomi.cargo;

public class BranchMasterCargo {
	
	private int branchId;
	private String branchCode;
	private String branchName;
	private String branchMain;
	private String branchAddress;
	private long branchPhno;
	private String branchCountry;
	private String branchState;
	private String branchCity;
	private String branchContactPer;
	private long branchMoNo;
	private String branchStatus;
	
	public int getBranchId() {
		return branchId;
	}
	public void setBranchId(int branchId) {
		this.branchId = branchId;
	}
	public String getBranchCode() {
		return branchCode;
	}
	public void setBranchCode(String branchCode) {
		this.branchCode = branchCode;
	}
	public String getBranchName() {
		return branchName;
	}
	public void setBranchName(String branchName) {
		this.branchName = branchName;
	}
	public String getBranchMain() {
		return branchMain;
	}
	public void setBranchMain(String branchMain) {
		this.branchMain = branchMain;
	}
	public String getBranchAddress() {
		return branchAddress;
	}
	public void setBranchAddress(String branchAddress) {
		this.branchAddress = branchAddress;
	}
	public long getBranchPhno() {
		return branchPhno;
	}
	public void setBranchPhno(long branchPhno) {
		this.branchPhno = branchPhno;
	}
	public String getBranchCountry() {
		return branchCountry;
	}
	public void setBranchCountry(String branchCountry) {
		this.branchCountry = branchCountry;
	}
	public String getBranchState() {
		return branchState;
	}
	public void setBranchState(String branchState) {
		this.branchState = branchState;
	}
	public String getBranchCity() {
		return branchCity;
	}
	public void setBranchCity(String branchCity) {
		this.branchCity = branchCity;
	}
	public String getBranchContactPer() {
		return branchContactPer;
	}
	public void setBranchContactPer(String branchContactPer) {
		this.branchContactPer = branchContactPer;
	}
	public long getBranchMoNo() {
		return branchMoNo;
	}
	public void setBranchMoNo(long branchMoNo) {
		this.branchMoNo = branchMoNo;
	}
	public String getBranchStatus() {
		return branchStatus;
	}
	public void setBranchStatus(String branchStatus) {
		this.branchStatus = branchStatus;
	}
}
